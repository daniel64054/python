"""
este programa reliza as 4 operações matematicas
"""
n1 = input("digite um número:")
n2 = input("digite outro número:")

#print(type(n1))
n1 = int(n1)
n2 = int(n2)
#print(type(n1))

soma = n1 + n2
subtracao = n1 - n2
mutiplicacao = n1 * n2
divisao = n1 / n2

print("resultado da soma é: " +str(soma))
print("resultado da subtração é: " +str(subtracao))
print("resultado da mutiplicação é: " +str(mutiplicacao))
print("resultado da divisão é: " +str(divisao))

